# trying LIFO with lists

trythis = [10, 20, 60, 90, 40, 8]
print('Printing the list first')
print(trythis)
print('Adding the integer')
trythis.append(int(input('Insert an integer: ')))
print(trythis)
print('Deleting the last integer')
trythis.pop()
print('Integer deleted successfully')
print(trythis)

print('Last In First Out(LIFO) executed successfully...')
